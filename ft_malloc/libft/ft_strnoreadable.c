#include "libft.h"

int		ft_strnoreadable(char *str)
{
	int		i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 127 || str[i] <= 31)
			return (1);
		i++;
	}
	return (0);
}
